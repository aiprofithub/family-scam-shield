function translateLevel(level, lang) {
  if (lang !== "th") return level;
  return {
    Critical: "สูงมาก",
    High: "สูง",
    Medium: "ปานกลาง",
    Low: "ต่ำ",
  }[level] || level;
}

function renderList(id, items) {
  const el = $(id);
  el.innerHTML = "";
  items.forEach((item) => {
    const li = document.createElement("li");
    li.textContent = item;
    el.appendChild(li);
  });
}

