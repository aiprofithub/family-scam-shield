function scoreClass(score) {
  if (score >= 80) return "score-critical";
  if (score >= 60) return "score-high";
  if (score >= 35) return "score-medium";
  return "score-low";
}

async function copyText(text) {
  const copied = state.lang === "th" ? "คัดลอกแล้ว" : "Copied.";
  const failed = state.lang === "th" ? "คัดลอกไม่สำเร็จ กรุณาเลือกข้อความเอง" : "Copy failed. Select the text manually.";
  try {
    await navigator.clipboard.writeText(text);
    alert(copied);
  } catch {
    alert(failed);
  }
}

