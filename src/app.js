const state = {
  lang: localStorage.getItem("fss-lang") || "en",
  theme: "dark",
};

const $ = (id) => document.getElementById(id);

function init() {
  applyTheme();
  applyLanguage();

  $("langEn").addEventListener("click", () => setLanguage("en"));
  $("langTh").addEventListener("click", () => setLanguage("th"));
  $("analyzeBtn").addEventListener("click", runAnalysis);
  $("loadExample").addEventListener("click", loadExample);
  $("loadHeroExample").addEventListener("click", loadExample);
  $("copyFamilyMessage").addEventListener("click", () => copyText($("familyMessage").innerText));
  $("copyShareCard").addEventListener("click", () => copyText($("shareCard").innerText));
}

function setLanguage(lang) {
  state.lang = lang;
  localStorage.setItem("fss-lang", lang);
  applyLanguage();
  if ($("messageInput").value.trim()) runAnalysis();
}

function applyLanguage() {
  const dict = translations[state.lang];
  document.documentElement.lang = state.lang;
  document.body.classList.toggle("lang-th", state.lang === "th");
  $("langEn").classList.toggle("active", state.lang === "en");
  $("langTh").classList.toggle("active", state.lang === "th");

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) el.setAttribute("placeholder", dict[key]);
  });

  if (!$("messageInput").value.trim()) renderEmpty();
}

function applyTheme() {
  document.documentElement.dataset.theme = "dark";
  localStorage.removeItem("fss-theme");
}



function loadExample() {
  $("messageInput").value = sampleText[state.lang] || sampleText.en;
  location.hash = "#checker";
  runAnalysis();
}

function runAnalysis() {
  const text = $("messageInput").value.trim();
  if (!text) {
    renderEmpty();
    return;
  }

  const result = analyzeMessage(text, state.lang);
  const steps = safeSteps(result.score, state.lang);
  const family = warningMessage(result, state.lang, $("warningLanguage").value);
  const card = buildShareCard(result, steps, family);

  const riskWord = state.lang === "th" ? "ความเสี่ยง" : "Risk";
  $("riskLabel").textContent = state.lang === "th"
    ? `${translateLevel(result.level, "th")} ${riskWord}`
    : `${result.level} risk`;

  $("scoreCircle").textContent = result.score;
  $("scoreCircle").className = `score-circle ${scoreClass(result.score)}`;
  $("summary").textContent = summaryFor(result.score, state.lang);

  renderList("redFlags", result.flags);
  renderList("nextSteps", steps);
  $("familyMessage").textContent = family;
  $("shareCard").textContent = card;
}

function analyzeMessage(text, lang) {
  const hits = rules.filter((rule) => rule.test(text));
  let score = hits.reduce((sum, rule) => sum + rule.weight, 0);

  const has = (id) => hits.some((hit) => hit.id === id);
  if (has("unknown-link") && has("payment-request")) score += 12;
  if (has("unknown-link") && has("credential-request")) score += 15;
  if (has("urgent-pressure") && (has("payment-request") || has("credential-request"))) score += 10;

  score = Math.min(100, score);
  const level = score >= 80 ? "Critical" : score >= 60 ? "High" : score >= 35 ? "Medium" : "Low";
  const flags = hits.length ? hits.map((hit) => hit[lang] || hit.en) : [
    lang === "th" ? "ยังไม่พบรูปแบบ scam ชัดเจนจากกฎพื้นฐานของ MVP นี้" : "No major scam pattern detected by this simple MVP"
  ];

  return { score, level, flags };
}

function summaryFor(score, lang) {
  if (lang === "th") {
    if (score >= 80) return "ข้อความนี้ดูเสี่ยงสูงมาก อย่ากดลิงก์ อย่าจ่ายเงิน และอย่าส่งรหัสส่วนตัว ให้ตรวจผ่านช่องทางทางการเท่านั้น";
    if (score >= 60) return "ข้อความนี้มีสัญญาณน่าสงสัยหลายจุด ควรถือว่าเสี่ยงจนกว่าจะตรวจสอบผ่านช่องทางทางการ";
    if (score >= 35) return "ข้อความนี้มีบางจุดที่ควรระวัง โดยเฉพาะลิงก์ การขอจ่ายเงิน หรือการยืนยันบัญชี";
    return "ยังไม่พบสัญญาณ scam ชัดเจน แต่ไม่ได้แปลว่าปลอดภัยเสมอ ตรวจสอบผู้ส่งก่อนทำรายการ";
  }

  if (score >= 80) return "This message looks highly unsafe. Do not click links, pay fees, or share private codes. Verify only through official channels.";
  if (score >= 60) return "This message has multiple warning signs. Treat it as suspicious until verified through an official app, website, or support channel.";
  if (score >= 35) return "This message has some suspicious signs. Be careful with links, payment requests, or account verification.";
  return "No strong scam pattern was found. That does not prove it is safe. Verify the sender before taking action.";
}

